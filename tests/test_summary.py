import unittest
# Импортируем тестируемые функции и саму модель/токензатор из твоего бота
from TGbot import get_summary, classify_risk_ml


class TestSummaryFunction(unittest.TestCase):

    def test_bureaucracy_skipping(self):
        """Проверка отсечения шапки документа и служебных слов"""
        text = "Кому: Директору ООО 'Вектор'\nОт: Иванова И.И.\nПросим Вас предоставить отчет."
        result = get_summary(text)
        self.assertTrue(result.lower().startswith("просим вас"))

    def test_date_preservation(self):
        """Проверка склеивания сути с предложением, содержащим дату дедлайна"""
        text = "Сообщаем о необходимости выполнения работ. Срок сдачи назначен на 25.12.2026."
        result = get_summary(text)
        self.assertIn("25.12.2026", result)

    def test_short_lines_exclusion(self):
        """Проверка удаления ФИО и коротких строк без знаков препинания"""
        text = "Главному инженеру\nПетрову А.В.\nНапоминаем о проведении совещания."
        result = get_summary(text)
        self.assertTrue(result.lower().startswith("напоминаем"))

    def test_too_short_text(self):
        """Проверка возврата заглушки при пустом или слишком коротком тексте"""
        text = "Привет"
        result = get_summary(text)
        self.assertEqual(result, "Не удалось извлечь содержание текста, рекомендуется ручной просмотр")


class TestMLRiskClassification(unittest.TestCase):
    """Набор тестов для проверки корректности предсказания уровней риска нейросетью RuBERT"""

    def test_red_risk_letter(self):
        """Проверочное письмо 1: Высокий уровень риска (Красный)"""
        # Текст письма, содержащий критические триггеры (срывы сроков, судебные иски, штрафы)
        text_red = (
            "Внимание! Нарушены существенные условия государственного контракта. "
            "В случае непредоставления проектной документации в течение 24 часов, "
            "Минстрой России оставляет за собой право расторгнуть соглашение в одностороннем порядке, "
            "направить сведения в реестр недобросовестных поставщиков (РНП) и обратиться "
            "в суд для взыскания штрафных санкций и неустойки в полном объеме."
        )
        risk_class, confidence = classify_risk_ml(text_red)

        # Проверяем, что модель распознала критическую ситуацию
        self.assertEqual(risk_class, "Красный", f"Ожидался Красный риск, модель выдала: {risk_class}")
        self.assertGreaterEqual(confidence, 0.5, "Уверенность модели слишком низкая для явного Красного риска")

    def test_yellow_risk_letter(self):
        """Проверочное письмо 2: Средний уровень риска (Желтый)"""
        # Текст письма, требующий планового контроля, рабочих изменений или предупреждения
        text_yellow = (
            "Уважаемые коллеги, доводим до вашего сведения, что в связи с изменением "
            "регламента согласования проектно-сметной документации, необходимо скорректировать "
            "графики производства работ на объектах капстроительства. Просим направить "
            "обновленные данные для анализа в рабочем порядке до конца текущей недели."
        )
        risk_class, confidence = classify_risk_ml(text_yellow)

        # Проверяем средний приоритет
        self.assertEqual(risk_class, "Желтый", f"Ожидался Желтый риск, модель выдала: {risk_class}")

    def test_green_risk_letter(self):
        """Проверочное письмо 3: Низкий уровень риска (Зеленый)"""
        # Информационное, ознакомительное, рутинное или поздравительное письмо
        text_green = (
            "Информируем Вас о проведении ежегодной научно-практической конференции "
            "'Цифровые технологии в строительстве и архитектуре'. Участие в мероприятии "
            "является добровольным. Программа конференции и список спикеров "
            "доступны во вложении к настоящему письму. Будем рады видеть вас среди участников."
        )
        risk_class, confidence = classify_risk_ml(text_green)

        # Проверяем низкий приоритет
        self.assertEqual(risk_class, "Зеленый", f"Ожидался Зеленый риск, модель выдала: {risk_class}")


if __name__ == '__main__':
    unittest.main()